type Message = {
    role: String,
    content: String,
    ui? : string
}

export type TripInfo={
    budget:string,
    destination:string,
    duration:string,
    group_size:string,
    origin:string,
    hotels: Hotel[],
    itinerary:Itinerary[]
}

export type Hotel ={
    hotel_name: string;
    hotel_address: string;
    price_per_night: string;
    hotel_image_url: string;
    geo_coordinates: {
        latitude: number;
        longitude: number;
    };
    rating: number;
    description: string
}

export type Activity={
    place_name: string;
    place_details: string;
    place_image_url: string;
    geo_coordinates: {
        latitude: number;
        longitude: number
    };
    place_address: string;
    ticket_pricing: string;
    time_travel_each_location: string,
    best_time_to_visit: string
}

type Itinerary={
    day: number;
    day_plan: string;
    best_time_to_visit_day: string;
    activities: Activity[]
}
function chatbox() {

    const [message, setMessage] = useState<Message[]>([]);
    const [userInput, setUserInput] = useState<string>('');
    const [Loading,setLoading]=useState(false);
    const [isFinal,setIsFinal]=useState(false);
    const [tripDetail,setTripDetail]=useState<TripInfo>();
   const SaveTripDetail = useMutation(api.tripDetails.CreateTripDetail)
   const {userDetail,setUserDetail}=useUserDetail();
   //@ts-ignore
   const {tripDetailInfo,setTripDetailInfo}=useTripDetail();
    const onSend = async () => {
        console.log('Inside');
        if (!userInput?.trim()) return;
        setLoading(true);
        setUserInput('');

        const newMessage: Message = {
            role: 'user',
            content: userInput
        };
        setMessage((prev: Message[]) => [...prev, newMessage]);
        const result = await axios.post('/api/aimodel', {
            message: [...message, newMessage],
            isFinal:isFinal

        });
        console.log("TRIP", result.data);
        !isFinal && setMessage((prev: Message[]) => [...prev, {
            role: 'assistant',
            content: result?.data?.resp,
            ui: result?.data?.ui
        }]);
        
        if(isFinal){
            setTripDetail(result?.data?.trip_plan);
            setTripDetailInfo(result?.data?.trip_plan);
            setIsFinal(false);
            const userId=uuidv4();
            await  SaveTripDetail({
                tripDetail:result?.data?.trip_plan,
                userId:userId,
                uid:userDetail?._id
            });


        }
        setLoading(false);
    }

    const renderGenerativeUi=(ui:string)=>{
        if(ui=='budget'){
            return <Budget onSelectedOption={(v:string)=>{setUserInput(v); onSend()}}/>
        }else if(ui=='groupSize'){
            return <GroupSize onSelectedOption={(v:string)=>{setUserInput(v); onSend()}}/>
        }else if(ui=='final'){
            return <FinalUi viewTrip={()=>console.log()}
            disable={!tripDetail}/>
        }
        return null;
    }

    useEffect(() => {
        const lastMsg=message[message.length - 1];
        if(lastMsg?.ui=='final'){
            setIsFinal(true);
            setUserInput('okay! Great');
            // onSend();
        }
    }, [message]);

    // useEffect(() => {
    //     if(isFinal && userInput){
    //         onSend();
    //     }
    // }, [isFinal]);
//     useEffect(() => {
//   const lastMsg = message[message.length - 1];
//   if (lastMsg?.ui == 'final') {
//     setIsFinal(true);
//     setUserInput('okay! Great');
//     onSend(); // single call only
//   }
// }, [message]);
    return (
        <div className='flex h-[85vh] flex-col'>
            {message?.length==0&&
            <EmptyBoxState/>}
            {/* display messages */}
            <section className='flex-1 overflow-y-auto p-4'>
                {message.map((msg: Message, index) => (
                    msg.role === 'user' ? (
                        <div className='flex justify-end mt-2' key={index}>
                            <div className='max-w-lg bg-primary text-white px-4 rounded-lg py-2'>
                                {msg.content}
                            </div>
                        </div>) : (
                        <div className='flex justify-start mt-2' key={index}>
                            <div className='max-w-lg bg-gray-200 text-black px-4 rounded-lg py-2'>
                               {msg.content}
                               {renderGenerativeUi(msg.ui ?? '')}
                            </div>
                        </div>)
                ))}
               {Loading && <div className='flex justify-start mt-2' >
                            <div className='max-w-lg bg-gray-200 text-black px-4 rounded-lg py-2'>
                               <Loader className='animate-spin'/>
                            </div>
                        </div>}
            </section>
            {/* user input box */}
            <section>
                <div className='border rounded-2xl p-4 relative'>
                    <Textarea placeholder='Start typing...'
                        className='w-full max-w-3xl h-28 bg-transparent border-black focus-visible:ring-0 shadow-none resize-none' onChange={(event) => setUserInput(event.target.value)} value={userInput} />
                    <Button size={'icon'} className='absolute bottom-6 right-6' onClick={() => onSend()}>
                        <Send className='h-4 w-4' /></Button>
                </div>
            </section>
        </div>
    )
}

export default chatbox

